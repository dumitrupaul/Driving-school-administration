    public DefaultTableModel model;
    public Interog5() {
        initComponents();
        model = (DefaultTableModel)jTable1.getModel();
        try{
            Connection cm = new Connection();
            
            ResultSet rs = cm.exQuery("select i.Nume,i.Prenume\n" +
"from Instructor i inner join InstructorMasina im on i.IDInstructor = im.IDInstructor\n" +
"                  inner join Masina m on m.IDMasina = im.IDMasina\n" +
"where '2018'-year(i.DataNasterii)>=50 and m.Model='Yamaha'");
            
            while(model.getRowCount()>0){
                model.removeRow(0);
            }
            
            while(rs.next()) {
                model.insertRow(model.getRowCount(),new Object[]{rs.getString("Nume"),rs.getString("Prenume")} );
            }
            cm.closeChannel(rs);
        }catch(SQLException ex){
            Logger.getLogger(ProiectBDFinal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

